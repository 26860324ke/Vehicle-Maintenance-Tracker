/**
 * Hook or local lookup function to translate static keys safely.
 */
export function getTranslation(key: string, lang: LanguageType): string {
  const dictionary = TRANSLATIONS[lang] || TRANSLATIONS['en'];
  return dictionary[key] || TRANSLATIONS['en'][key] || key;
}

/**
 * Format currency value natively with support for CAD and TWD
 */
export function formatCurrencyValue(amount: number, currency: CurrencyType, lang: LanguageType): string {
  const locale = lang === 'zh-TW' ? 'zh-TW' : 'en-US';
  
  // Custom display configurations
  try {
    return new Intl.NumberFormat(locale, {
      style: 'currency',
      currency: currency,
      currencyDisplay: 'symbol',
      minimumFractionDigits: currency === 'TWD' ? 0 : 2,
      maximumFractionDigits: currency === 'TWD' ? 0 : 2
    }).format(amount);
  } catch (err) {
    // Elegant fallback formatting if locales / currencies are not loaded
    const symbolMap = { TWD: 'NT$', CAD: 'C$' };
    const symbol = symbolMap[currency] || '$';
    const formattedNum = amount.toLocaleString(undefined, {
      minimumFractionDigits: currency === 'TWD' ? 0 : 2,
      maximumFractionDigits: currency === 'TWD' ? 0 : 2
    });
    return `${symbol}${formattedNum}`;
  }
}
